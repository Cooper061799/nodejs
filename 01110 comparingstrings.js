"use strict";

let text = 'cat';

console.log("text == 'cat'", text == 'cat'); // equal operator
console.log("text === 'cat'", text === 'cat'); // identity operator

console.log("text != 'cat'", text != 'cat') // not equal operator
console.log("text !== 'cat'", text !== 'cat') // not identical operator
